def _principal_to_dict(p: PrincipalAccess) -> dict:
    """Convert a PrincipalAccess to a serialisable dict for JSON.

    Intentionally omits ``allowed_actions`` to avoid leaking raw policy
    details.  Only the derived ``access_level`` is included.
    """
    d: dict = {
        "principal_type": p.principal_type.value,
        "principal_name": p.principal_name,
        "access_level": p.access_level.value,
        "last_accessed": _serialise_last_accessed(p.last_accessed),
        "policy_source": p.policy_source,
    }

    # Classification-specific fields
    if p.classification == PrincipalClassification.EKS_SERVICE_ACCOUNT:
        d["is_service_account"] = True
        d["service_account_detail"] = p.eks_detail
        d["identity_center_user"] = None
        d["identity_center_group"] = None
    elif p.ic_resolution is not None:
        d["is_service_account"] = False
        d["service_account_detail"] = None
        d.update(_ic_to_dict(p.ic_resolution))
    else:
        d["is_service_account"] = False
        d["service_account_detail"] = None
        d["identity_center_user"] = None
        d["identity_center_group"] = None

    return d


def _ic_to_dict(ic: IdentityCenterResolution) -> dict:
    """Convert IC resolution to dict fields for structured output.

    Unlike table format, structured output includes the full member list
    (no truncation).
    """
    users_list = []
    for u in ic.users:
        entry: dict = {
            "user_id": u.user_id,
            "display_name": u.display_name,
            "email": u.email,
            "deleted": u.deleted,
        }
        if u.via_group is not None:
            entry["via_group"] = u.via_group
        users_list.append(entry)

    groups_list = []
    for g in ic.groups:
        members = []
        for m in g.members:
            members.append({
                "user_id": m.user_id,
                "display_name": m.display_name,
                "email": m.email,
                "deleted": m.deleted,
            })
        groups_list.append({
            "group_id": g.group_id,
            "group_name": g.group_name,
            "total_member_count": g.total_member_count,
            "members": members,
        })

    return {
        "permission_set_name": ic.permission_set_name,
        "identity_center_user": users_list if users_list else None,
        "identity_center_group": groups_list if groups_list else None,
        "ic_partial": ic.partial,
    }


def _serialise_last_accessed(value: datetime | str | None) -> str | None:
    """Serialise last_accessed for structured output."""
    if value is None:
        return None
    if isinstance(value, datetime):
        return value.isoformat()
    return str(value)


def _report_to_dict(report: AuditReport) -> dict:
    """Convert the full report to a serialisable dict."""
    m = report.metadata
    return {
        "secret_name": m.secret_name,
        "secret_arn": m.secret_arn,
        "region": m.region,
        "generated_at": m.generated_at,
        "generated_by": m.generated_by,
        "tool_version": m.tool_version,
        "warnings": report.warnings if report.warnings else [],
        "principals": [_principal_to_dict(p) for p in report.principals],
        "versions": [
            {
                "version_id": v.version_id,
                "staging_labels": v.staging_labels,
                "created_date": _serialise_last_accessed(v.created_date),
            }
            for v in report.versions
        ],
    }


def render_json(report: AuditReport) -> str:
    """Render JSON with ``indent=2``.

    Full group member lists (no truncation).
    """
    data = _report_to_dict(report)
    return json.dumps(data, indent=2, default=str)


