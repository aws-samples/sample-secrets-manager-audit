"""Report output formatting — table, CSV, JSON, and PDF renderers.

Renders an :class:`~secrets_audit.models.AuditReport` into one of four
formats: human-readable table (modeled on AWS CLI ``--output table``),
JSON, CSV, or PDF.

**Security invariant**: rendered output never contains secret values,
full IAM policy JSON, raw STS credentials, or session tokens.  The
``allowed_actions`` list is intentionally omitted from structured output
to avoid leaking policy details — only the derived ``access_level``
string is included.
"""

from __future__ import annotations

import csv
import io
import json
import logging
from datetime import datetime

from secrets_audit.models import (
    AuditReport,
    ICGroupResolution,
    IdentityCenterResolution,
    PrincipalAccess,
    PrincipalClassification,
)

logger = logging.getLogger(__name__)

# Maximum group members shown in table format before truncation
_TABLE_GROUP_MEMBER_LIMIT = 50


def render(report: AuditReport, output_format: str) -> str | bytes:
    """Dispatch to the appropriate renderer.

    Parameters
    ----------
    report:
        The fully-enriched audit report.
    output_format:
        One of ``"table"``, ``"json"``, ``"csv"``, or ``"pdf"``.

    Returns
    -------
    str | bytes
        The rendered report string (or bytes for PDF).

    Raises
    ------
    ValueError
        If *output_format* is not recognised.
    """
    dispatch: dict[str, object] = {
        "table": render_table,
        "json": render_json,
        "csv": render_csv,
        "pdf": render_pdf,
    }
    renderer = dispatch.get(output_format)
    if renderer is None:
        raise ValueError(f"Unknown output format: {output_format!r}")
    return renderer(report)


def render_table(report: AuditReport) -> str:
    """Render human-readable table with metadata header and columnar data.

    Columns: PRINCIPAL TYPE, PRINCIPAL NAME, IC USER / GROUP,
    ACCESS LEVEL, LAST ACCESSED.

    Group members are truncated at 50 in table format with
    ``"...and N more members"``.
    """
    lines: list[str] = []

    # --- Metadata header ---
    m = report.metadata
    lines.append(f"Secret: {m.secret_name}")
    lines.append(f"ARN:    {m.secret_arn}")
    lines.append(f"Region: {m.region if m.region is not None else '(default)'}")
    lines.append(f"Report generated: {m.generated_at}")
    lines.append(f"Generated by:     {m.generated_by}")
    lines.append(f"Tool:             {m.tool_version}")
    lines.append("")

    # --- Warnings ---
    for warning in report.warnings:
        lines.append(f"WARNING: {warning}")
    if report.warnings:
        lines.append("")

    # --- Empty principals ---
    if not report.principals:
        lines.append("No IAM principals have access to this secret")
        _append_versions_section(lines, report)
        return "\n".join(lines)

    # --- Column definitions ---
    headers = [
        "PRINCIPAL TYPE",
        "PRINCIPAL NAME",
        "IC USER / GROUP",
        "ACCESS LEVEL",
        "LAST ACCESSED",
    ]

    # Build rows — each principal produces one or more display rows.
    all_rows: list[list[str]] = []
    for p in report.principals:
        primary = _table_row(p)
        continuation = _ic_continuation_rows(p)
        all_rows.append(primary)
        all_rows.extend(continuation)

    # Calculate column widths
    widths = [len(h) for h in headers]
    for row in all_rows:
        for i, cell in enumerate(row):
            widths[i] = max(widths[i], len(cell))

    # Format header line
    header_line = "   ".join(h.ljust(widths[i]) for i, h in enumerate(headers))
    separator = "   ".join("-" * widths[i] for i in range(len(headers)))
    lines.append(header_line)
    lines.append(separator)

    # Format data rows
    for row in all_rows:
        line = "   ".join(row[i].ljust(widths[i]) for i in range(len(headers)))
        lines.append(line)

    # --- Secret versions section ---
    _append_versions_section(lines, report)

    return "\n".join(lines)


def _table_row(p: PrincipalAccess) -> list[str]:
    """Build a single table row for a principal."""
    principal_type = p.principal_type.value
    principal_name = p.principal_name
    ic_col = _ic_column_table(p)
    access_level = p.access_level.value
    last_accessed = _format_last_accessed(p.last_accessed)
    return [principal_type, principal_name, ic_col, access_level, last_accessed]


def _ic_column_table(p: PrincipalAccess) -> str:
    """Derive the IC USER / GROUP column value for table output.

    Returns only the primary line. Group member details are rendered as
    continuation rows by :func:`_ic_continuation_rows`.
    """
    if p.classification == PrincipalClassification.EKS_SERVICE_ACCOUNT:
        return "Service Account (EKS)"

    if p.ic_resolution is None:
        return "N/A"

    ic = p.ic_resolution

    # If partial (cross-account failed), show permission set name only
    if ic.partial:
        return f"PS: {ic.permission_set_name}"

    parts: list[str] = []

    # Direct users
    for user in ic.users:
        if user.via_group is None:
            name = user.email or user.display_name or user.user_id
            parts.append(name)

    # Groups (name only — members shown as continuation rows)
    for group in ic.groups:
        parts.append(f"Group: {group.group_name}")

    # Group-based users (direct users assigned via group)
    for user in ic.users:
        if user.via_group is not None:
            name = user.email or user.display_name or user.user_id
            parts.append(f"{name} (via group {user.via_group})")

    if not parts:
        return f"PS: {ic.permission_set_name}"

    return ", ".join(parts)


def _ic_continuation_rows(p: PrincipalAccess) -> list[list[str]]:
    """Build continuation rows for group members in table output."""
    if p.ic_resolution is None or p.ic_resolution.partial:
        return []

    ic = p.ic_resolution
    rows: list[list[str]] = []

    for group in ic.groups:
        shown = group.members[:_TABLE_GROUP_MEMBER_LIMIT]
        for member in shown:
            name = member.email or member.display_name or member.user_id
            status = "Deleted" if member.deleted else "Enabled"
            rows.append(["", "", f"  {name} ({status})", "", ""])

        remaining = group.total_member_count - _TABLE_GROUP_MEMBER_LIMIT
        if remaining > 0:
            rows.append(["", "", f"...and {remaining} more members", "", ""])

    return rows


def _format_group_members_table(group: ICGroupResolution) -> list[str]:
    """Format group members for table output, truncating at 50."""
    if not group.members:
        return []

    shown = group.members[:_TABLE_GROUP_MEMBER_LIMIT]
    parts = [m.display_name or m.user_id for m in shown]

    remaining = group.total_member_count - _TABLE_GROUP_MEMBER_LIMIT
    if remaining > 0:
        parts.append(f"...and {remaining} more members")

    return parts


def _format_last_accessed(value: datetime | str | None) -> str:
    """Format the last_accessed field for display."""
    if value is None:
        return "N/A"
    if isinstance(value, datetime):
        return value.strftime("%Y-%m-%d %H:%M UTC")
    return str(value)


def _append_versions_section(lines: list[str], report: AuditReport) -> None:
    """Append the SECRET VERSIONS table section if versions are present."""
    if not report.versions:
        return

    lines.append("")
    lines.append("SECRET VERSIONS")
    v_headers = ["VERSION ID", "STAGING LABELS", "CREATED DATE"]
    v_rows: list[list[str]] = []
    for v in report.versions:
        labels = ", ".join(v.staging_labels)
        date_str = _format_last_accessed(v.created_date)
        v_rows.append([v.version_id, labels, date_str])

    v_widths = [len(h) for h in v_headers]
    for vr in v_rows:
        for i, cell in enumerate(vr):
            v_widths[i] = max(v_widths[i], len(cell))

    v_header_line = "   ".join(h.ljust(v_widths[i]) for i, h in enumerate(v_headers))
    v_separator = "   ".join("-" * v_widths[i] for i in range(len(v_headers)))
    lines.append(v_header_line)
    lines.append(v_separator)
    for vr in v_rows:
        lines.append(
            "   ".join(vr[i].ljust(v_widths[i]) for i in range(len(v_headers)))
        )


