def render_csv(report: AuditReport) -> str:
    """Render CSV with metadata as comment lines and principal data rows.

    For IC group members (when expand-groups produced group data), adds
    continuation rows with ``IC Group`` as the principal type.
    """
    buf = io.StringIO()

    # Metadata as comment lines
    m = report.metadata
    buf.write(f"# Secret: {m.secret_name}\n")
    buf.write(f"# ARN: {m.secret_arn}\n")
    buf.write(f"# Region: {m.region or '(default)'}\n")
    buf.write(f"# Report generated: {m.generated_at}\n")
    buf.write(f"# Generated by: {m.generated_by}\n")
    buf.write(f"# Tool: {m.tool_version}\n")

    writer = csv.writer(buf)
    headers = [
        "Principal Type",
        "Principal Name",
        "IC User / Group",
        "Access Level",
        "Last Accessed",
    ]
    writer.writerow(headers)

    for p in report.principals:
        ic_col = _ic_column_table(p)
        writer.writerow([
            p.principal_type.value,
            p.principal_name,
            ic_col,
            p.access_level.value,
            _format_last_accessed(p.last_accessed),
        ])

        # IC group member continuation rows
        if p.ic_resolution and not p.ic_resolution.partial:
            for group in p.ic_resolution.groups:
                for member in group.members:
                    email = member.email or member.display_name or member.user_id
                    status = "DISABLED" if member.deleted else "ENABLED"
                    writer.writerow([
                        "IC Group",
                        group.group_name,
                        email,
                        status,
                        "",
                    ])

    return buf.getvalue()


