def render_pdf(report: AuditReport) -> bytes:
    """Render a PDF report using ReportLab.

    Returns raw PDF bytes.  The layout mirrors the terminal table format
    but formatted for US Letter paper.
    """
    from reportlab.lib import colors
    from reportlab.lib.pagesizes import letter
    from reportlab.lib.styles import getSampleStyleSheet
    from reportlab.platypus import (
        Paragraph,
        SimpleDocTemplate,
        Spacer,
        Table,
        TableStyle,
    )

    buf = io.BytesIO()
    doc = SimpleDocTemplate(buf, pagesize=letter)
    styles = getSampleStyleSheet()
    elements: list = []

    def _esc(text: str) -> str:
        """Escape text for ReportLab Paragraph XML parser."""
        return (
            text.replace("&", "&amp;")
            .replace("<", "&lt;")
            .replace(">", "&gt;")
        )

    # --- Metadata header ---
    m = report.metadata
    for line in [
        f"<b>Secret:</b> {_esc(m.secret_name)}",
        f"<b>ARN:</b> {_esc(m.secret_arn)}",
        f"<b>Region:</b> {_esc(m.region or '(default)')}",
        f"<b>Report generated:</b> {_esc(m.generated_at)}",
        f"<b>Generated by:</b> {_esc(m.generated_by)}",
        f"<b>Tool:</b> {_esc(m.tool_version)}",
    ]:
        elements.append(Paragraph(line, styles["Normal"]))
    elements.append(Spacer(1, 12))

    # --- Warnings ---
    for warning in report.warnings:
        elements.append(Paragraph(f"<b>WARNING:</b> {_esc(warning)}", styles["Normal"]))
    if report.warnings:
        elements.append(Spacer(1, 12))

    # --- Principals table ---
    if not report.principals:
        elements.append(Paragraph("No IAM principals have access to this secret", styles["Normal"]))
    else:
        headers = [
            "Principal Type",
            "Principal Name",
            "IC User / Group",
            "Access Level",
            "Last Accessed",
        ]
        data = [headers]
        for p in report.principals:
            data.append([
                p.principal_type.value,
                p.principal_name,
                _ic_column_table(p),
                p.access_level.value,
                _format_last_accessed(p.last_accessed),
            ])

        table = Table(data, repeatRows=1)
        table.setStyle(TableStyle([
            ("BACKGROUND", (0, 0), (-1, 0), colors.grey),
            ("TEXTCOLOR", (0, 0), (-1, 0), colors.whitesmoke),
            ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
            ("FONTSIZE", (0, 0), (-1, -1), 8),
            ("GRID", (0, 0), (-1, -1), 0.5, colors.black),
            ("VALIGN", (0, 0), (-1, -1), "TOP"),
        ]))
        elements.append(table)

    # --- Versions table ---
    if report.versions:
        elements.append(Spacer(1, 12))
        elements.append(Paragraph("<b>Secret Versions</b>", styles["Heading3"]))
        v_headers = ["Version ID", "Staging Labels", "Created Date"]
        v_data = [v_headers]
        for v in report.versions:
            v_data.append([
                v.version_id,
                ", ".join(v.staging_labels),
                _format_last_accessed(v.created_date),
            ])
        v_table = Table(v_data, repeatRows=1)
        v_table.setStyle(TableStyle([
            ("BACKGROUND", (0, 0), (-1, 0), colors.grey),
            ("TEXTCOLOR", (0, 0), (-1, 0), colors.whitesmoke),
            ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
            ("FONTSIZE", (0, 0), (-1, -1), 8),
            ("GRID", (0, 0), (-1, -1), 0.5, colors.black),
        ]))
        elements.append(v_table)

    doc.build(elements)
    return buf.getvalue()
